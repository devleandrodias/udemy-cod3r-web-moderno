//Isso é um operador de atribuição
const a = 7;

let b = 3;
 
//Atribuição aditiva
const resut = b += a; //b = b + a 
 console.log(resut);

//Atribuição subtrativa
const result2 = b -= a;
console.log(result2);

//Atribuição multiplcativa
const result3 = b *= a;
console.log(result3);

//Atribuição divitiva
const result4 = b /= a;
console.log(result4);

//Atribuição modular
const result5 = b %= 2;
console.log(result5);